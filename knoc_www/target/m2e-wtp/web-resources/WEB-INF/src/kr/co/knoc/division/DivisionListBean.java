package kr.co.knoc.division;

public class DivisionListBean {
	private String d_code;
	private String d_name;
	private String d_tell;
	
	public String getDCode() {
		return this.d_code;
	}
	public void setDCode(String d_code) {
		this.d_code = d_code;
	}
	
	public String getDName() {
		return this.d_name;
	}
	public void setDName(String d_name) {
		this.d_name = d_name;
	}
	
	public String getDTell() {
		return this.d_tell;
	}
	public void setDTell(String d_tell) {
		this.d_tell = d_tell;
	}
}
