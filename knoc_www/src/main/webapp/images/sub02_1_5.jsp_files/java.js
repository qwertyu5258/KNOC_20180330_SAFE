function premier(n) {
 document.getElementById('premier'+n).style.display = "block";
    for(var i = 1; i < 4; i++) {
        obj = document.getElementById('premier'+i);
      //  img = document.getElementById('premier_button'+i);
        if ( n != i ) {
            obj.style.display = "none";   
          //  img.src = "mainimg/check_0"+i+"on.gif";            
        }
    }
}
